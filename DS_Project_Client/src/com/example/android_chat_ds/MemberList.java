public class MemberList {
    private String username;  	
    private String tag;  

    public String getName() {  
        return username;  
    }  

    public void setName(String name) {  
        this.username = name;  
    }  
    
    public String getTag() {  
       return tag;       
   }  
    
    public void setTag(String tag) {  
        this.tag = tag;  
   }  	
}
